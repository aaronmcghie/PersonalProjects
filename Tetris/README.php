<p>I chose not to find a partner for this assignment because I had very little time this week to spend at school, and I did a large portion of my work at home at odd times. I had an issue fixing a bug when rotating pieces near the left or right edge.  I spent a very long time trying to fix it before deciding there were more important things I should spend my time on. I also left a bug, where when you start a new game after losing, it tells you that you lost as soon as you press the down key; this bug came up at the end of my development, and I tried to debug it for a little while, but I didn't feel as though I had the time or energry to solve it.</p>
